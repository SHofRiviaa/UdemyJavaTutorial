package Section4_Maven;

public class MavenReview {
    public static void main(String args [])
    {
        // Maven is a build automation tool used primarily for Java projects.

        // Maven addresses two aspects of building software:
        // 1. It describes how software is built.
        // 2. It describes its dependencies.

        // Maven is a project management tool that is based on POM (Project Object Model).
        // It can manage a project's build, reporting, and documentation from a central piece of information.

        // Maven is a tool that can now be used for building and managing any Java-based project.
        // We can also build projects written in C#, Ruby, Scala, and other languages.

        // Getting dependencies from Maven Central Repository
        // Maven Central Repository is a repository that is provided by the Apache Software Foundation.
        // It is used by Maven and other tools to download and manage dependencies.

        // Example of a Maven dependency
        // <dependency>
        //     <groupId>org.apache.commons</groupId>
        //     <artifactId>commons-lang3</artifactId>
        //     <version>3.12.0</version>
        // </dependency>

        // Maven archetypes are project templates that can be used to create new projects.
        // Maven archetypes are used to create new projects with a specific structure and configuration.

        // How maven works
        // 1. Maven reads the pom.xml file.
        // 2. It downloads the dependencies from the Maven Central Repository.

        // Be sure to use safe and secure dependencies in your project.
        // Always check the dependencies for vulnerabilities.
    }
}
